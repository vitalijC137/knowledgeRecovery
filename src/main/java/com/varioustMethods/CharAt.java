package com.varioustMethods;

public class CharAt {

    public void charAtMethod() {
        String sentence = "I love java so much";
        char ch1 = sentence.charAt(0);
        char ch2 = sentence.charAt(5);
        char ch3 = sentence.charAt(9);
        System.out.println("index letter 0 is: " + ch1);
        System.out.println("index letter 5 is: " + ch2);
        System.out.println("index letter 9 is: " + ch3);
    }
}
