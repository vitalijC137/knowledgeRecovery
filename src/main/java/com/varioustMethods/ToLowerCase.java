package com.varioustMethods;

public class ToLowerCase {
    public void toLowerCaseMethod() {
        String string = "I love JAVA";
        String result = string.toLowerCase();
        System.out.println("Result: " + result);

        string = "TEXT 1 2 3";
        result = string.toLowerCase();
        System.out.println("Result: " + result);

        string = "vertex";
        result = string.toLowerCase();
        System.out.println("Result: " + result);
    }
}
